  // multiply c5 by x and add P[6] (degree 4)
  a_mul (h, l, c5, x);
  fast_two_sum (h, &t, P[6], *h);
  *l += t;
  // multiply (h,l) by x and add P[4]+P[5] (degree 3)
  s_mul (h, l, x, *h, *l);
