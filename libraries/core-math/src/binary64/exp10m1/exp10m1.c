// returns a double-double approximation hi+lo of exp(x*log(10))
// assumes -0x1.041704c068efp+4 < x <= 0x1.34413509f79fep+8
static inline void exp_2 (double *hi, double *lo, double x) {

  double eh, el;
#define INVLOG2_10 0x1.a934f0979a371p+13 // 2^12*log(10)/log(2)
  double k = roundeven_finite (x * INVLOG2_10); // -221184 <= k <= 4194304
  if (__builtin_expect (k == 4194304, 0))
    k = 4194303; // ensures M < 2047 below

  double yh, yl;
  /* LOG2H+LOG2_10M+LOG2L approximates log(2)/log(10)/2^12, with LOG2_10M
     representable on 30 bits so that LOG2_10M*k is exact */
#define LOG2_10H 0x1.34413509f79ffp-14
#define LOG2_10M -0x1.9dc1da98p-71
#define LOG2_10L -0x1.4fd20dba1f655p-103
  yh = __builtin_fma (-k, LOG2_10H, x); // exact
  fast_two_sum (&yh, &yl, yh, -k * LOG2_10M);
  yl = __builtin_fma (-k, LOG2_10L, yl);
  // now multiply yh+yl by log(10)
  s_mul (&eh, &el, yh, LN10H, LN10L);
  yh = eh;
  yl = __builtin_fma (yl, LN10H, el);

  int64_t K = k;
  int64_t M = (K >> 12) + 0x3ff;
  int64_t i2 = (K >> 6) & 0x3f;
  int64_t i1 = K & 0x3f;

  double t1h = T1[i2][0], t1l = T1[i2][1], t2h = T2[i1][0], t2l = T2[i1][1];
  d_mul (hi, lo, t2h, t2l, t1h, t1l);

  double qh, ql;
  q_2 (&qh, &ql, yh, yl);

  d_mul (hi, lo, *hi, *lo, qh, ql);
  f64_u _d;

  _d.u = M << 52;
  if (__builtin_expect (M == 0x7ff, 0))
  {
    *hi *= 2.0;
    *lo *= 2.0;
    _d.u = (M-1) << 52;
  }
  *hi *= _d.f;
  *lo *= _d.f;
}

