  double c9 = __builtin_fma (P[12], x, P[11]); // degree 9
  double c7 = __builtin_fma (P[10], x, P[9]);  // degree 7
  double c5 = __builtin_fma (P[8], x, P[7]);   // degree 5
  c9 = __builtin_fma (P[13], x2, c9);          // degree 9
  c5 = __builtin_fma (c7, x2, c5);             // degree 5
  c5 = __builtin_fma (c9, x4, c5);             // degree 5
