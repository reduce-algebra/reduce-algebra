__bf16 cr_expm1_bf16 (__bf16 x){
  b16u16_u v = {.f = x};
  uint16_t u = v.u, au = u & 0x7fff;

  if (au <= 0x3b00) { // |x| <= 2^-9
    if (au == 0) return x; // x = 0
#ifdef CORE_MATH_SUPPORT_ERRNO
    /* we have underflow for |x| < 2^-126 and for x=-0x1p-126 and rounding
       towards zero */
    if (au < 0x80 || (u == 0x8080 && __builtin_fmaf (x, -0x1p-25f, x) != x))
      errno = ERANGE; // underflow
#endif
    // avoid a spurious underflow for x=-0x1p-126
    if (u == 0x8080) return __builtin_fmaf (x, x, x); // x = -0x1p-126
    return (au == u) ? x * 0x1.000002p+0f : x * 0x1.fffffep-1f;
  }

  if (au >= 0x42ba) { // |x| >= 0x1.74p+6
    if (au >= 0x7f80) // NaN or Inf
      return (u == 0xff80) ? -1.0f : x + x;
#ifdef CORE_MATH_SUPPORT_ERRNO
    if (au == u) errno = ERANGE; // overflow
#endif
    return (au == u) ? x * 0x1p127f : -0x1.fffffep-1f;
  }

  // now 0x3b00 < au < 0x42ba thus the range of au fits on 11 bits
  // more precisely the exponent part lies in [118,133]
  uint16_t i1 = ((u >> 15) << 8) + (au >> 3) - 0x760;
  uint16_t i2 = ((u >> 15) << 7) + (((au >> 7) << 3) | (au & 0x7)) - 0x3b0;
#ifdef CORE_MATH_SUPPORT_ERRNO
  if (au == u && au >= 0x42b2) // x >= 0x1.64p+6
    errno = ERANGE; // overflow
#endif
  return __builtin_fmaf (T1[i1], T2[i2], -1.0f);
}

