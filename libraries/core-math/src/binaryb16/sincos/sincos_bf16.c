void cr_sincos_bf16 (__bf16 x, __bf16 *s, __bf16 *c){
  b16u16_u v = {.f = x};
  uint16_t u = v.u, au = u & 0x7fff;

  static const float sgn[] = {1.0, -1.0};

  // for |x| <= 0x1p-4, sin(x) rounds to x to nearest and cos(x) rounds to 1
  if (au <= 0x3d80) { // |x| <= 0x1.dp-4
    if (au == 0) { // x = +0 or -0
      *s = x;
      *c = 1.0f;
      return;
    }
    // avoid spurious underflow for |x|=0x1p-126
    *s = (au == 0x80) ? sgn[u>>15] * 0x1.fffffcp-127f
      : x * 0x1.fffffep-1f;
    *c = 0x1.fffffep-1f;
#ifdef CORE_MATH_SUPPORT_ERRNO
    if (__builtin_fabsf (*s) < 0x1p-126)
      errno = ERANGE; // underflow
#endif
    return;
  }

  // now 0x3d00 <= au, |x| > 0x1p-4
  
  if (au < 0x4580) { // |x| < 4096
    // the exponent part lies in [123,138]
    uint16_t i1 = (au - 0x3d80) >> 3; // 0x3d80 is 123<<7
    uint16_t i2 = (((au - 0x3d80) >> 7) << 3) | (au & 0x7);
    // we use a FMA to fix the evaluation order
    *s = sgn[u>>15] * __builtin_fmaf (S1[i1], C2[i2], C1[i1] * S2[i2]);
    *c = __builtin_fmaf (C1[i1], C2[i2], -S1[i1] * S2[i2]);
    return;
  }

  if ((au >> 7) == 0xff) { // NaN or Inf
    b16u16_u qnan = {.u = 0x7fc0};
    *s = *c = qnan.f + x;
    return;
  }

  /* now |x| >= 4096 thus ulp(x) >= 32.
     We write |x| = b0*2^i + ... + b7*2^(i+7).
     We tabulate the values of sin(2^i) and cos(2^i) for 5 <= i < 128 and use
     the relations:
     sin(a+b) = sin(a)*cos(b) + cos(a)*sin(b)
     cos(a+b) = cos(a)*cos(b) - sin(a)*sin(b) */

  int k = (au >> 7) - 0x8b; // k=0 for |x|=4096
  float sj = S3[k+7], cj = C3[k+7]; // sin/cos of b7*2^(i+7)
  for (int j = 0; j < 7; j++)
    if ((au >> j) & 1) {
      float t = __builtin_fmaf (sj, C3[k+j], cj * S3[k+j]);
      cj = __builtin_fmaf (cj, C3[k+j], -sj * S3[k+j]);
      sj = t;
    }
  *s = sgn[u>>15] * sj;
  *c = cj;
}

