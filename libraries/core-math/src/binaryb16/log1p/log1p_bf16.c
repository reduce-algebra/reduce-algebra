// approximates log(1+x) for x small
static inline float log1p_small (float x) {
  float c3 = __builtin_fmaf (x, -0x1p-2f, 0x1.555556p-2f);
  float c2 = __builtin_fmaf (x, c3, -0.5f);
  float res = __builtin_fmaf (x, x * c2, x);
  return res;
}

__bf16 cr_log1p_bf16 (__bf16 x){
  b32u32_u v = {.f = x};
  uint32_t u = v.u, au = u & 0x7fffffff;

  static const b16u16_u nan = {.u = 0x7fc0}; // qNaN
  static const b16u16_u inf = {.u = 0x7f80}; // +Inf
  static const b16u16_u minus_inf = {.u = 0xff80}; // -Inf

  if (u >= 0xbf800000u) { // x <= -1
#ifdef CORE_MATH_SUPPORT_ERRNO
    // for x=-1, we have errno=ERANGE (overflow to -Inf)
    errno = (u == 0xbf800000u) ? ERANGE : EDOM;
#endif
    // x + nan.f raises invalid if x = -sNaN
    return (u == 0xbf800000u) ? minus_inf.f : x + nan.f;
  }

  if ((u >> 23) == 0xff) return inf.f + x; // NaN or Inf

  /* for x >= 0x1.6p+11, log1p(x) rounds to the same value as log(x),
     cf check_large() in log1p.sage */
  if (au >= 0x45300000u) { // |x| >= 0x1.6p+11
    uint16_t i1 = u >> 23;
    uint16_t i2 = (u >> 16) & 0x7f;
    return T1[i1] + T2[i2];
  }

  // for |x| <= 0x1.68p-8, log1p(x) rounds to the same value as x - x^2/2
  if (au <= 0x3bb40000u) {
    float t = v.f;
    float res = (au == 0) ? x : __builtin_fmaf (t, -t * 0.5f, t);
#ifdef CORE_MATH_SUPPORT_ERRNO
    // we have underflow for |x| < 2^-126, and for x = 2^-126 and rounding towards zero
    if (au != 0 && __builtin_fabsf (res) < 0x1p-126f)
      errno = ERANGE; // underflow
#endif
    return res;
  }

  // now 0x1.68p-8 < |x| < 0x1.6p+11 thus 1+x is exact as a float
  v.f += 1.0f; // exact
  int i = v.u >> 20; // 928 <= i <= 1143
  i -= 928;
  float z = __builtin_fmaf (R[i][0], v.f, -1.0f); // exact
  return R[i][1] + log1p_small (z);
}

