__bf16 cr_exp2_bf16 (__bf16 x){
  b16u16_u v = {.f = x};
  uint16_t u = v.u, au = u & 0x7fff;

  // for -0x1.7p-9 <= x <= 0x1.7p-8, exp2(x) rounds to x to nearest
  if (au <= 0x3b38) { // |x| <= 0x1.7p-9
    if (au == 0) return 1.0f16; // x = +0 or -0
    return 1.0f + ((au == u) ? 0x1p-24f : -0x1p-25f);
  }

#ifdef CORE_MATH_SUPPORT_ERRNO
  if (au >= 0x4300 && au == u) // x >= 128
    errno = ERANGE; // overflow
  if (au > 0x42fc && au != u) // x < -126
    if (u != 0xc2fe && (u < 0xc300 || u > 0xc305)) // x != {-127,...,-133}
      errno = ERANGE; // underflow
#endif

  // for x >= 128, exp2(x) >= 2^128
  // for x < -0x1.0cp+7, exp(x) < 2^-134
  if (au >= 0x4300) { // |x| >= 128
    if (au >= 0x7f80) { // case Inf or NaN
      if (u == 0xff80) return 0; // -Inf
      return x + x; // +Inf or NaN
    }
    if (au == u) return 0x1p+127f + 0x1p+127f; // overflow
    // now x <= -128
    if (au >= 0x4306)
      return 0x1p-134f; // underflow
    // it remains 0xc300 <= u < 0xc306 with x negative
    static const float und[] = {0x1p-128f, 0x1p-129f, 0x1p-130f, 0x1p-131f,
                                0x1p-132f, 0x1p-133f};
    return und[u - 0xc300];
  }

  // now 0x3b38 < au < 0x4300 thus the range of au fits on 11 bits
  // more precisely the exponent part lies in [118,133]
  uint16_t i1 = ((u >> 15) << 8) + (au >> 3) - 0x760;
  uint16_t i2 = ((u >> 15) << 7) + (((au >> 7) << 3) | (au & 0x7)) - 0x3b0;
  return T1[i1] * T2[i2];
}

