__bf16 cr_exp2m1_bf16 (__bf16 x){
  b16u16_u v = {.f = x};
  uint16_t u = v.u, au = u & 0x7fff;

#define LOG2 0x1.62e43p-1f  // approximates log(2)

  if (au <= 0x3b38) { // |x| <= 0x1.7p-9
    if (au == 0) return x; // x = 0
#ifdef CORE_MATH_SUPPORT_ERRNO
    /* we have underflow for |x| <= 0x1.7p-126, except when |x| = 0x1.7p-126
       and rounding away from zero */
    if (au < 0xb8 || (au == 0xb8 && __builtin_fmaf (x, 0x1p-25f, x) == x))
      errno = ERANGE; // underflow
#endif
    // avoid a spurious underflow for |x| = 0x1.7p-126
    if (au == 0xb8) return (au == u) ? 0x1.fe3p-127f : -0x1.fe3p-127f;
    // Taylor expansion of 2^x-1 is log(2)*x + log(2)^2/2*x^2
    float t = LOG2 * x;
    return (au <= 0x138) ? t : __builtin_fmaf (t, 0.5f * t, t);
  }

  if (au >= 0x4300) { // |x| >= 128
    if (au >= 0x7f80) // NaN or Inf
      return (u == 0xff80) ? -1.0f : x + x;
#ifdef CORE_MATH_SUPPORT_ERRNO
    // we have overflow for x > 0, except for x=128 and rounding towards zero
    if (au == u && (au != 0x4300 || __builtin_fmaf (x, -0x1p-25f, x) == x))
      errno = ERANGE; // overflow
#endif
    return (au == u) ? x * 0x1.fffffep+120f : -0x1.fffffep-1f;
  }

  // now 0x3b38 < au < 0x4300 thus the range of au fits on 11 bits
  // more precisely the exponent part lies in [118,133]
  uint16_t i1 = ((u >> 15) << 8) + (au >> 3) - 0x760;
  uint16_t i2 = ((u >> 15) << 7) + (((au >> 7) << 3) | (au & 0x7)) - 0x3b0;
  return __builtin_fmaf (T1[i1], T2[i2], -1.0f);
}

