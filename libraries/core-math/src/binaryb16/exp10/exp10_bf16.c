__bf16 cr_exp10_bf16 (__bf16 x){
  b16u16_u v = {.f = x};
  uint16_t u = v.u, au = u & 0x7fff;

  // for -0x1.bcp-11 <= x <= 0x1.bap-10, exp10(x) rounds to x to nearest
  if (au < 0x3a80) { // |x| < 0x1p-10
    if (au == 0) return 1.0f16; // x = +0 or -0
    if (au == u) return 0x1.000002p+0; // 0 < x <= 0x1p-10
    if (au <= 0x3a5e) return 0x1.fffffep-1f; // -0x1.bcp-11 <= x < 0
    // now -0x1p-10 < x < -0x1.bcp-11 thus 0xba5f <= u <= 0xba7f
    return T3[u - 0xba5f];
  }

#ifdef CORE_MATH_SUPPORT_ERRNO
  if (au >= 0x421b && au == u) // x >= 0x1.36p+5
    errno = ERANGE; // overflow
  if (au >= 0x4218 && au != u) // x <= -0x1.3p+5
    errno = ERANGE; // underflow
#endif

  // for x >= 0x1.36p+5, exp10(x) >= 2^128
  // for x <= -0x1.44p+5, exp10(x) < 2^-134
  if (au >= 0x4222) { // |x| >= 0x1.44p+5
    if (au >= 0x7f80) { // case Inf or NaN
      if (u == 0xff80) return 0; // -Inf
      return x + x; // +Inf or NaN
    }
    return (au == u) ? 0x1p+127f + 0x1p+127f : 0x1p-134f;
  }

  // now 0x3a80 <= au < 0x4222 thus the range of au fits on 11 bits
  // more precisely the exponent part lies in [117,132]
  uint16_t i1 = ((u >> 15) << 8) + (au >> 3) - 0x750;
  uint16_t i2 = ((u >> 15) << 7) + (((au >> 7) << 3) | (au & 0x7)) - 0x3a8;
  float res = T1[i1] * T2[i2];
  return res;
}

