__bf16 cr_tanpi_bf16 (__bf16 x){
  b16u16_u v = {.f = x};
  uint16_t u = v.u, au = u & 0x7fff;

  if (au >= 0x3f00) { // |x| >= 1/2 thus x might integer or half-integer
    if (au >= 0x7f80) // NaN or Inf
      return (0x7f81 < au && au < 0x7fc0) ? x + x : 0.0f / 0.0f;
    static const uint16_t M[] = { 0xffff, 0x7f, 0x3f, 0x1f, 0xf, 0x7, 0x3, 0x1, 0x0 };
    int e = au >> 7; // 126 <= e <= 254
    if (e >= 134 || (au & M[e-126]) == 0) { // x is integer
      // tanpi(2n+1) = -0,  tanpi(2n+2) = +0
      // tanpi(-2n-1) = +0, tanpi(-2n-2) = -0
      /* for 2^7 <= |x| < 2^8, where ulp(x)=1, we have e = 134,
         thus the fractional part is made of the 134-e low bits of au */
      int b = e >= 135 || (au & (1<<(134-e))) == 0; // b=1 iff x is even
      int c = au == u;                              // c=1 iff x > 0
      return (b == c) ? +0.0f : -0.0f;
    }
    else if ((au & M[e-125]) == 0) { // x is half-integer
      /* The case |x|=1/2 is special since the bit 1/2 is hidden in the
         implicit bit. */
      int b = e == 126 || (au & (1<<(134-e))) == 0;
      // b=1 iff |x|=2n+1/2
      int c = au == u;                            // c=1 iff x > 0
#ifdef CORE_MATH_SUPPORT_ERRNO
      errno = ERANGE; // overflow
#endif
      return (b == c) ? +1.0f / 0.0f : -1.0f / 0.0f;
    }
  }

  // now |x| < 2^6 and x is not an integer nor half an integer

  static const float sgn[] = {1.0, -1.0};
  if (au < 0x3b00) { // |x| < 2^-9
#define PI24 0x1.921fb6p+1f
    if (au == 0) return x; // x = +0 or -0
    float res;
    // pi*x works except for 0x1.c4p-13, 0x1.c4p-12, 0x1.c4p-11, 0x1.c4p-10
    switch (au) {
    case 0x3962: return sgn[u>>15] * 0x1.630002p-11f; // 0x1.c4p-13
    case 0x39e2: return sgn[u>>15] * 0x1.63000cp-10f; // 0x1.c4p-12
    case 0x3a62: return sgn[u>>15] * 0x1.630036p-9f;  // 0x1.c4p-11
    case 0x3ae2: return sgn[u>>15] * 0x1.6300e2p-8f;  // 0x1.c4p-10
    }
    res = PI24 * (float) x;
#ifdef CORE_MATH_SUPPORT_ERRNO
    if (__builtin_fabsf (res) < 0x1p-126)
      errno = ERANGE; // underflow
#endif
    return res;
  }

  // now 2^-9 <= |x| < 2^6
  // the exponent part lies in [118,132]
  uint16_t i1 = (au - 0x3b00) >> 3; // 0x3b00 is 118<<7, i1 < 240
  uint16_t i2 = (((au - 0x3b00) >> 7) << 3) | (au & 0x7);
  // we have i1 < 240 and i2 < 120
  // we use a FMA to fix the evaluation order
  float s = __builtin_fmaf (S1[i1], C2[i2], C1[i1] * S2[i2]);
  float c = __builtin_fmaf (C1[i1], C2[i2], -S1[i1] * S2[i2]);
  return sgn[u>>15] * (s / c);
}

